% y3=[8 8 9 9 2 2 4 4 4 4 1 1 13 13 11 11 9 9 5 5 9 9 7 7 15 15 12 12 11 11 13 13];        %x3、y3、x4、y4 为所有障碍物边界的相邻顶点组成的线段。
