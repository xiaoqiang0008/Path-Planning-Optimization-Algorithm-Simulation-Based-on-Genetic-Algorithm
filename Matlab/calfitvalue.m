for j=1:1:px
    in=inpolygon(x(j,:),y(j,:),xv,yv);   %判断 点 是否在障碍物内
    for i=1:1:py-1
        x1(i)=x(j,i);
        y1(i)=y(j,i);
    end
    for i=2:1:py
        x2(i-1)=x(j,i);
        y2(i-1)=y(j,i);
    end
